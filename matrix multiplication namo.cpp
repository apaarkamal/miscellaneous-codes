const ll N = 105;
struct matrix {
  ll B[N][N], n;
  matrix() {n = N; memset(B, 0, sizeof B);}
  matrix(ll _n) {
    n = _n; memset(B, 0, sizeof B);
  }
  void iden() {
    for (ll i = 0; i < n; i++)
      B[i][i] = 1;
  }
  void operator += (matrix M) {
    for (ll i = 0; i < n; i++)
      for (ll j = 0; j < n; j++)
        B[i][j] = add(B[i][j], M.B[i][j]);
  }
  void operator -= (matrix M) {
    for (ll i = 0; i < n; i++)
      for (ll j = 0; j < n; j++)
        B[i][j] = sub(B[i][j], M.B[i][j]);
  }
  void operator *= (ll b) {
    for (ll i = 0; i < n; i++)
      for (ll j = 0; j < n; j++)
        B[i][j] = mul(b, B[i][j]);
  }
  matrix operator - (matrix M) {
    matrix ret = (*this);
    ret -= M; return ret;
  }
  matrix operator + (matrix M) {
    matrix ret = (*this);
    ret += M; return ret;
  }
  matrix operator * (matrix M) {
    matrix ret = matrix(n); memset(ret.B, 0, sizeof ret.B);
    for (ll i = 0; i < n; i++)
      for (ll j = 0; j < n; j++)
        for (ll k = 0; k < n; k++) {
          ret.B[i][j] = add(ret.B[i][j], mul(B[i][k], M.B[k][j]));
        }
    return ret;
  }
  matrix operator *= (matrix M) { *this = ((*this) * M);}
  matrix operator * (ll b) {
    matrix ret  = (*this); ret *= b; return ret;
  }
  vector<ll> multiply(const vector<ll> & v) const {
    vector<ll> ret(n);
    for (ll i = 0; i < n; i++)
      for (ll j = 0; j < n; j++) {
        ret[i] = add(ret[i], mul(B[i][j] , v[j]));
      }
    return ret;
  }
};




